
package gestionetablissement;


public class Semaines {
 private int idSemaine;
 private int  noSemaine; //a chnager avec un tableau

    public Semaines(int idSemaine, int noSemaine) {
        this.idSemaine = idSemaine;
        this.noSemaine = noSemaine;
    }

    public int getIdSemaine() {
        return idSemaine;
    }

    public void setIdSemaine(int idSemaine) {
        this.idSemaine = idSemaine;
    }

    public int getNoSemaine() {
        return noSemaine;
    }

    public void setNoSemaine(int noSemaine) {
        this.noSemaine = noSemaine;
    }


 
    
}
